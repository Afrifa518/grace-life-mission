// 3. TEACHING MINISTRY
// • Declare the whole counsel of God to all men 
// • Teach men to deny ungodliness and worldly lust and to live 
// soberly, righteousness and godly in this present age. 
// 4. Take dominion of our community and world at large through the 
// establishment of the kingdom of God 
// 5. Prepare the church towards the second coming of Christ. 
